# "while" haqida
son = 1 # o'zgaruvchi 1 dan boshlanadi
          # shuning uchun ham o'zgaruvchiga 1 qiymat beriladi
while son <= 31: # [ ogohlantiruv ] { : } 2 nuqta belgisini yozish shart
# { while } [o'zgaruvchining nomi }
       # va o'zgaruvchi 31 sonidan dan kichikmi tekshiriladi
    print(son)
    son = son + 1 #
    # [o'zgaruvchining nomi] { = } [o'zgaruvchining nomi]
    # ogohlantiruv { + 1 } yozish shart
